package Controllers;

/**
 * Addition Class
 * Allow user to addition two numbers
 * @author Mathieu K
 */
public class Addition{
	
	public Addition() {}
	
	/**
	 * Addition Method which addition two numbers 
	 * @param a Double value
	 * @param b Double value
	 */
	public double Addition(double a, double b) {
		return a + b;
	}
}
