package Controllers;

/**
 * Substraction Class
 * Allow user to substract two numbers
 * @author Mathieu K
 */
public class Substraction {

	public Substraction() {}
	
	/**
	 * Substraction Method which subtract two numbers 
	 * @param nbr1 Double value
	 * @param nbr2 Double value
	 */
	public double Substraction(Double nbr1, Double nbr2) {
		return nbr1 - nbr2;
	}
}
